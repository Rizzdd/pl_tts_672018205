
package db_kosan;

public class tb_DataKos {
    private String id_data_kos;
    private String no_kamar;
    private String ukuran_kamar;
    private String harga_kos;

    public String getId_data_kos() {
        return id_data_kos;
    }

    public void setId_data_kos(String id_data_kos) {
        this.id_data_kos = id_data_kos;
    }

    public String getNo_kamar() {
        return no_kamar;
    }

    public void setNo_kamar(String no_kamar) {
        this.no_kamar = no_kamar;
    }

    public String getUkuran_kamar() {
        return ukuran_kamar;
    }

    public void setUkuran_kamar(String ukuran_kamar) {
        this.ukuran_kamar = ukuran_kamar;
    }

    public String getHarga_kos() {
        return harga_kos;
    }

    public void setHarga_kos(String harga_kos) {
        this.harga_kos = harga_kos;
    }

    
}
