
package db_kosan;

public class tb_DataPribadi {
    private String id_pribadi;
    private String nama;
    private String tgl_masuk;
    private String no_hp;
    private String no_kamar;
    private String alamat_asal;

    public String getId_pribadi() {
        return id_pribadi;
    }

    public void setId_pribadi(String id_pribadi) {
        this.id_pribadi = id_pribadi;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getTgl_masuk() {
        return tgl_masuk;
    }

    public void setTgl_masuk(String tgl_masuk) {
        this.tgl_masuk = tgl_masuk;
    }

    public String getNo_hp() {
        return no_hp;
    }

    public void setNo_hp(String no_hp) {
        this.no_hp = no_hp;
    }

    public String getNo_kamar() {
        return no_kamar;
    }

    public void setNo_kamar(String no_kamar) {
        this.no_kamar = no_kamar;
    }

    public String getAlamat_asal() {
        return alamat_asal;
    }

    public void setAlamat_asal(String alamat_asal) {
        this.alamat_asal = alamat_asal;
    }
    
    
}
